    <h2>Zubereitung</h2>
    <ol class="steps noselect">
        <li><span class="highlight">Butter und Zucker</span> schaumig mischen.</li>
        <li><span class="highlight">Eier</span> dazurühren. (Je länger man die Eier mixt, desto luftiger und trockener wird er. Also möglichst kurz mixen.) Dann die <span class="highlight">Milch und schliesslich das Mehl</span>.</li>
        <li><span class="highlight">Backpulver, Vanillezucker und Salz</span> unterrühren.</li>
        <li>Kuchenform mit Butter ausstreichen und mit Mehl bestäuben.</li>
        <li>Bei <span class="highlight">170 °C für 50 bis 55 Minuten</span> backen.</li>
        <li>Zum Auskühlen Alufolie drüberstülpen.</li>
    </ol>
    
