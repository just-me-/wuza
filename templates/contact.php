  var o = {
    init: function(){
      $( "#diagram svg" ).remove();
      this.diagram();
    },
    random: function(l, u){
      return Math.floor((Math.random()*(u-l+1))+l);
    },
    diagram: function(){
      var width = $('#diagram').width();
      var height = (width < 620) ? width : 620;
      var r = Raphael('diagram', width, height),
        rad = 59,
        defaultText = 'Know-how',
        speed = 250;
      
      r.circle(width/2, height/2, 65).attr({ stroke: 'none', fill: '#193340' });
      
      var title = r.text(width/2, height/2, defaultText).attr({
        font: '16px Arial',
        fill: '#fff'
      }).toFront();
      
      r.customAttributes.arc = function(value, color, rad){
        var v = 3.6*value,
          alpha = v == 360 ? 359.99 : v,
          random = o.random(91, 240),
          a = (random-alpha) * Math.PI/180,
          b = random * Math.PI/180,
          sx = width/2 + rad * Math.cos(b),
          sy = height/2 - rad * Math.sin(b),
          x = width/2 + rad * Math.cos(a),
          y = height/2 - rad * Math.sin(a),
          path = [['M', sx, sy], ['A', rad, rad, 0, +(alpha > 180), 1, x, y]];
        return { path: path, stroke: color }
      }
      
      $('.get').find('.arc').each(function(i){
        var t = $(this), 
          color = t.find('.color').val(),
          value = t.find('.percent').val(),
          text = t.find('.text').text(),
          subtext = t.find('.subtext').val();
        
        rad += 15;	
        var z = r.path().attr({ arc: [value, color, rad], 'stroke-width': 12 });
        
        z.mouseover(function(){
          this.animate({ 'stroke-width': 15, opacity: .75 }, 1000, 'elastic');
          if(Raphael.type != 'VML') //solves IE problem
          this.toFront();
          title.stop().animate({ opacity: 0 }, speed, '>', function(){
            this.attr({ text: text + (subtext ? "\n " + subtext : "") }).animate({ opacity: 1 }, speed, '<');
          });
        }).mouseout(function(){
          this.stop().animate({ 'stroke-width': 12, opacity: 1 }, speed*4, 'elastic');
          title.stop().animate({ opacity: 0 }, speed, '>', function(){
            title.attr({ text: defaultText }).animate({ opacity: 1 }, speed, '<');
          });	
        });
      });
      
    }
  }
  $(function(){ o.init(); });
  $( window ).resize(function() { o.init(); });
  // end raphael chart
</script>
