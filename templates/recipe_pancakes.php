    <h1><span class="bubbles">Pancakes</span></h1>
    
    <h2>Zutaten</h2>
    <ul class="ingredients checkable noselect">
        <li>180g Mehl</li>
        <li>1 EL Zucker</li>
        <li>1 TL Backpulver</li>
        <li>¼ TL Salz</li>
        <li>¼ TL Natron</li>
        <li>2 Eier</li>
        <li>250ml Milch</li>
        <li>3 EL Öl</li>
        <li>Butter für die Pfanne</li>
        <li>Ahornsirup zum Servieren</li>
    </ul>
    
    <h2>Zubereitung</h2>
    <ol class="steps">
        <li>Die <span class="highlight">trockenen Zutaten</span> grob mischen, dann den <span class="highlight">Rest</span> dazugeben und gut umrühren.</li>
        <li>Etwas Butter in die Pfanne und dann Teigkreise bei mittlerer Kitze anbraten, bis sich Bläschien bilden. Dann wenden.</li>
    </ol>
    
</div>
