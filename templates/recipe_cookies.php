    <h1><span class="bubbles">American Cookies</span></h1>
    
    <h2>Zutaten</h2>
    <ul class="ingredients checkable noselect">
        <li>110g Butter</li>
        <li>170g Zucker</li>
        <li>1 Päckchen Vanillezucker</li>
        <li>½ TL Salz</li>
        <li>1 Ei</li>
        <li>100g Milchschokolade</li>
        <li>50g dunkle Schokoladenwürfelchen</li>
        <li>165g Mehl</li>
        <li>½ TL Backpulver</li>
    </ul>
    
    <h2>Zubereitung</h2>
    <ol class="steps">
        <li><span class="highlight">Butter</span> grob mixen. Dann <span class="highlight">Zucker, Vanillezucker, Salz und Eier</span> dazugeben.</li>
        <li>Erst die <span class="highlight">Schokolade</span>, dann das <span class="highlight">Mehl und Backpulver</span> darunterrühren.</li>
        <li>Den Teig mit 2 Teelöffeln auf dem Blech anordnen. Mit nassen Fingern die Plätzchen etwas flach drücken.</li>
        <li>Bei <span class="highlight">175 °C ca. 12 Minuten</span> backen, bis der Boden braun ist. Dann abkühlen lassen, damit sie hart werden.</li>
    </ol>
    
</div>
