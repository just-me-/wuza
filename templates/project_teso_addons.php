    <p>
        Das Add-on gibt es über <a href="https://github.com/just-me-/MerlinsRezHelper" target=_blank>GitHub</a>
        oder direkt von <a href="http://www.esoui.com/downloads/info1600-MerlinsRezHelper.html" target=_blank>esoui.com</a>. 
    </p>
    <p>
        Im Eifer des Gefechts kann es ab und zu mal vorkommen, dass man wegen all den Effekten tote Gruppenmitglieder
        nur schwer findet oder teils gar nicht bemerkt, dass jemand den Löffel abgegeben hat.
        Im High-End-Content kann aber jede Sekunde zählen, die gebraucht wird, bis ein Gruppenmitglied wiederbelebt wurde.
    </p>
    <p>
        Genau da setzt dieses Add-on an; es hilft dem Spieler, tote Gruppenmitglieder so schnell wie möglich
        wiederzubeleben und so in den Kampf zurückzubringen.
        Sobald ein Gruppenmitglied gestorben ist, wird ein Pfeil angezeigt, welcher zum nächstgelegenen, toten Gruppenmitglied navigiert.
        Optional ändert sich das Anzeigesymbol sogar, sobald man in der unmittelbaren Nähe eines Spielers steht.
        Dies soll dabei helfen, dass man nicht aus Versehen <i>auf</i> einem toten Spieler steht, ohne es zu bemerken. 
        Falls niemand in der Gruppe tot ist, wird natürlich auch kein Pfeil angezeigt.
    </p>
    <p>
        Es gibt eine Vielfalt an Optionen für die Pfeildarstellungen; für dessen Layout, Grösse und Transparenz.
        Ausserdem gibt es verschiedene Soundbenachrichtigungen zur Auswahl, welche auf frisch gestorbene Gruppenmitglieder hinweisen.
        Diese Erweiterung unterstützt - sowie all meine Add-ons - nebst der Sprache Deutsch auch Englisch. 
    </p>
    
