    <p>
        Spielt man die Rolle des Heilers, so ist diese Erweiterung eine optimale, aber dezente Hilfe.
        Sobald das Leben eines Gruppenmitglieds unter einen definierten Prozentwert sinkt,
        wird sofort eine Benachrichtigung eingeblendet.
        Falls jemand ausser Reichweite ist und Heilung benötigt, wird ebenfalls eine (andere) Nachricht angezeigt.
        Sobald die nötige Heilung gewirkt wurde, verschwindet die Meldung natürlich wieder. 
    </p>
    
