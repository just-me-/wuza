        Mit diesem Add-on werden die DPS-Werte aller Gruppenmitglieder mit der gesamten Gruppe oder
        nur mit der Raidleitung geteilt. Zusätzlich liefert diese Erweiterung auch kleine
        Hilfen für die Raidleitung wie beispielsweise einen Pausentimer, durch welchen man
        einen Timer bei allen Raidtailnehmer initialisieren kann,
        sowie eine rdy-Check-Funktion, durch welche die Namen der noch nicht wieder anwesenden Teilnehmer gelistet werden. 
